/* global initAdvancedTable */

/**
 * Render functions for Reports table columns
 * Exported for testing purposes
 */

/**
 * Render report ID as a link to report detail page
 * @param {number|string} val - The ID value
 * @param {Object} row - The row data
 * @returns {string} HTML link string
 */
function renderReportId(val, row) {
  return `<a href="/reports/${row.id}">${val}</a>`;
}

/**
 * Render report type with a badge
 * @param {string} val - The report type value
 * @returns {string} HTML badge string
 */
function renderReportType(val) {
  const typeColors = {
    shift_report: "primary",
    weekend_report: "success",
  };
  const displayName =
    val === "shift_report" ? "Shift Report" : "Weekend Report";
  const color = typeColors[val] || "secondary";
  return `<span class="badge bg-${color}">${displayName}</span>`;
}

/**
 * Render shift info
 * @param {string} val - The shift value
 * @returns {string} Formatted shift
 */
function renderShift(val) {
  if (!val) return "N/A";
  const colors = { Early: "warning", Night: "dark" };
  const color = colors[val] || "secondary";
  return `<span class="badge bg-${color}">${val}</span>`;
}

/**
 * Get column configuration for Reports table
 * @returns {Array} Column configuration array
 */
function getReportsColumns() {
  return [
    {
      key: "id",
      label: "ID",
      type: "number",
      render: renderReportId,
    },
    {
      key: "title",
      label: "Title",
      type: "text",
    },
    {
      key: "report_type",
      label: "Type",
      type: "text",
      render: renderReportType,
    },
    {
      key: "shift",
      label: "Shift",
      type: "text",
      render: renderShift,
    },
    { key: "generated_by_name", label: "Generated By", type: "text" },
    { key: "generated_on", label: "Generated On", type: "datetime" },
  ];
}

/**
 * Initialize Reports table
 */
function initReportsTable() {
  const reportsDataElement = document.getElementById("reports-data");
  if (reportsDataElement) {
    const reportsData = JSON.parse(reportsDataElement.textContent);
    initAdvancedTable("reportsTable", reportsData, getReportsColumns(), 25);
  }
}

// Initialize table on DOM load (browser only)
if (typeof document !== "undefined") {
  document.addEventListener("DOMContentLoaded", initReportsTable);
}

// Export for testing (CommonJS)
if (typeof module !== "undefined" && module.exports) {
  module.exports = {
    renderReportId,
    renderReportType,
    renderShift,
    getReportsColumns,
    initReportsTable,
  };
}
